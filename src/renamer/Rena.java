    public void listarArchivos(String dir) {

        File jarsFolder = new File(dir);
        File[] imas = jarsFolder.listFiles();
        for (int i = 0; i < imas.length; i++) {
            if (!imas[i].getName().startsWith(".")) {
                modelo.addElement(imas[i].getName());
            }

        }

        jList1.setModel(modelo);
    }

    public void rename(String oldname, String newname) {
        File oldfile = new File(jTextField1.getText() + oldname);
        File newfile = new File(jTextField1.getText() + newname);

        oldfile.renameTo(newfile);
        System.out.println(newfile.getName());
    }

